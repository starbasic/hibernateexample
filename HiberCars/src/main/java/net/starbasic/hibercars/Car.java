package net.starbasic.hibercars;

/**
 *
 * @author omp
 */
public class Car {
    private int id;
	private String model;
	private int year;
	private int price;
	private User owner;
    
	public Car(){
   	 
	}
        
        public Car(String model, int year, int price, User owner){
            this.model = model;
            this.owner = owner;
            this.price = price;
            this.year = year;
        }
    

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the model
     */
    public String getModel() {
        return model;
    }

    /**
     * @param model the model to set
     */
    public void setModel(String model) {
        this.model = model;
    }

    /**
     * @return the year
     */
    public int getYear() {
        return year;
    }

    /**
     * @param year the year to set
     */
    public void setYear(int year) {
        this.year = year;
    }

    /**
     * @return the price
     */
    public int getPrice() {
        return price;
    }

    /**
     * @param price the price to set
     */
    public void setPrice(int price) {
        this.price = price;
    }

    /**
     * @return the owner
     */
    public User getOwner() {
        return owner;
    }

    /**
     * @param owner the owner to set
     */
    public void setOwner(User owner) {
        this.owner = owner;
    }
    
    @Override
    public String toString(){
        return model + " " + year + "  " + price + "  " + owner.getLogin();
    }

}
